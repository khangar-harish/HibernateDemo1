package com.hash;

public class SchoolStudent extends Student{
	
	private String board;
	private String type;
	
	
	public SchoolStudent() {
	}
	public String getBoard() {
		return board;
	}
	public void setBoard(String board) {
		this.board = board;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
